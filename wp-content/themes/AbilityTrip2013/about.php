				<h1>More information about Ability Trip</h1>
				<p>AbilityTrip was co-founded in 2008 by husband and wife team Darren and Faith Brehm. In 1993, Darren and Faith were both seriously injured in a vehicle rollover accident which rendered Darren with a high level spinal cord injury resulting in quadriplegia. Determined not to let Darren’s injury define or limit their possibilities, Darren and Faith have continued to live life to its fullest, working and traveling around the world. In addition to his duties at AbilityTrip, Darren is the Associate Director of Ingredients: Food Additives at Kraft Foods. Prior to his role with Kraft, he was an Engagement Manager with McKinsey &amp; Company. He holds a B.S. in Finance from SDSU and a MBA with distinction from Harvard Business School. Faith is acting Site Manager of AbilityTrip and is a graduate of UMass, Boston, with a B.A. in Anthropology.</p>
				<h2>Founders</h2>
				<p>Early into their travels, Darren and Faith found it very difficult to plan trips, as accessible information on logistics, hotels, and activities of a destination are scattered throughout the web. Worse, some destinations have little to no accessible information available online. AbilityTrip was created in hopes of making knowledge on accessible travel easy to find and update for every destination around the world for all types of physically challenged individuals. Planning a trip should not be a painful experience, or involve hours of digging through sites all over the web.</p>

				<p>In June 2009, Melissa Hayes joined the AbilityTrip team. Melissa is a perfect fit, as she has both a passion for helping others and for travel, having visited 45 US states and over 20 countries. She also has an impressive resume of successful ventures, and is eager to help AbilityTrip achieve its goal of being the primary resource for accessible travel. Melissa Hayes holds a B.A. in Economics from Northwestern and a MBA from Harvard Business School.</p>

				<p>AbilityTrip’s mission is to serve all segments of physically challenged individuals and their companions: from the visually and hearing impaired, to the wheelchair and scooter user, to the active senior with mobility issues. We at AbilityTrip highly encourage the sharing of personal experience. In fact, this community will only succeed if we all collectively pool our travel knowledge together. Please register and submit your accessible travel information for any destination you have visited. If the destination is not currently listed, add your knowledge to the most relevant category and we will create the page, or email us your experience and we will post your update to the appropriate category.</p>

				<p>There is a large, diverse world out there. Let’s get out, explore it, and share our knowledge! – <span>Safe travels! Darren, Faith, and Melissa</span></p>
